#!/usr/bin/env node
/**
 * Frontend skill integration eval runner.
 *
 * Reads eval.json, counts tokens (gpt-tokenizer cl100k_base; falls back to chars/4)
 * for each scenario across approach A, approach B, and the pre-integration baseline,
 * prints a comparison table, and writes results.md.
 *
 * Usage:  node skills/sdlc/integration/run-eval.mjs
 */
import { readFileSync, writeFileSync } from "node:fs";
import { join, dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const EVAL_DIR = __dirname;
const SDLC_ROOT = resolve(EVAL_DIR, ".."); // skills/sdlc
const EVAL_FILE = join(EVAL_DIR, "eval.json");
const RESULTS_FILE = join(EVAL_DIR, "results.md");

// --- tokenizer (graceful fallback) ---
let encode = null;
let tokenizerLabel = "gpt-tokenizer (cl100k_base)";
let estimated = false;
try {
  const mod = await import("gpt-tokenizer");
  encode = mod.encode;
  // smoke test
  if (typeof encode("hello world").length !== "number") throw new Error("bad");
} catch {
  encode = (text) => Math.ceil(Buffer.byteLength(text, "utf8") / 4);
  tokenizerLabel = "chars/4 (gpt-tokenizer unavailable — ESTIMATED)";
  estimated = true;
}

const evalJson = JSON.parse(readFileSync(EVAL_FILE, "utf8"));
const root = evalJson.root; // "skills/sdlc"

function readTokens(relPath) {
  const abs = join(SDLC_ROOT, relPath);
  let text;
  try {
    text = readFileSync(abs, "utf8");
  } catch {
    return { tokens: 0, bytes: 0, missing: true };
  }
  return { tokens: encode(text).length, bytes: Buffer.byteLength(text, "utf8"), missing: false };
}

function resolveOnDemand(onDemand) {
  if (Array.isArray(onDemand)) return onDemand;
  if (onDemand === "shared_on_demand") return evalJson.shared_on_demand.files;
  if (onDemand && typeof onDemand === "object" && onDemand.files) return onDemand.files;
  return [];
}

const fmt = (n) => (estimated ? `~${n.toLocaleString()}` : n.toLocaleString());

// --- shared on-demand token block (identical for A and B) ---
const sharedFiles = evalJson.shared_on_demand.files;
const sharedBlock = sharedFiles.map((f) => ({ path: f, ...readTokens(f) }));
const sharedTotal = sharedBlock.reduce((s, f) => s + f.tokens, 0);

// --- cost evals ---
const costRows = [];
for (const ev of evalJson.cost_evals) {
  const row = { id: ev.id, mode: ev.mode, description: ev.description };
  for (const approach of ["A", "B"]) {
    const cfg = ev[approach];
    const always = (cfg.always_loaded || []).map((f) => ({ path: f, ...readTokens(f) }));
    const onDemand = resolveOnDemand(cfg.on_demand).map((f) => ({
      path: f,
      ...readTokens(f),
    }));
    const alwaysTotal = always.reduce((s, f) => s + f.tokens, 0);
    const onDemandTotal = onDemand.reduce((s, f) => s + f.tokens, 0);
    row[approach] = {
      always,
      onDemand,
      alwaysTotal,
      onDemandTotal,
      total: alwaysTotal + onDemandTotal,
    };
  }
  row.delta = row.B.total - row.A.total; // + means B costs more
  costRows.push(row);
}

// --- baseline (before integration) ---
const baselineSkills = evalJson.baseline.skills.map((s) => {
  const skillMd = { path: s.skill_md, ...readTokens(s.skill_md) };
  const refs = (s.refs || []).map((f) => ({ path: f, ...readTokens(f) }));
  const total = skillMd.tokens + refs.reduce((a, r) => a + r.tokens, 0);
  return { skill: s.skill, skillMd, refs, total };
});
const baselineTotal = baselineSkills.reduce((s, x) => s + x.total, 0);
const baselineFileCount =
  baselineSkills.length + baselineSkills.reduce((a, s) => a + s.refs.length, 0);

// --- report ---
const lines = [];
lines.push("# Integration eval results");
lines.push("");
lines.push(
  `> Generated by \`run-eval.mjs\`. Tokenizer: **${tokenizerLabel}**. Numbers are the skill content pulled into context per scenario — see \`eval-methodology.md\`. Absolute counts are indicative; the **A vs B vs baseline** comparison is the point.`,
);
lines.push("");

lines.push("## Headline");
lines.push("");
lines.push("| Scenario | A (tokens) | B (tokens) | B − A | Cheaper |");
lines.push("|---|---:|---:|---:|---|");
for (const r of costRows) {
  const cheaper = r.delta < 0 ? "**B**" : r.delta > 0 ? "**A**" : "tie";
  lines.push(
    `| ${r.id} | ${fmt(r.A.total)} | ${fmt(r.B.total)} | ${fmt(r.delta)} | ${cheaper} |`,
  );
}
lines.push("");
lines.push(`**Baseline (6 source skills, fully loaded):** ${fmt(baselineTotal)} tokens across ${baselineFileCount} files.`);
const afterSingle = costRows.find((r) => r.id === "S1-build-landing");
const cut = Math.round((1 - afterSingle.A.total / baselineTotal) * 100);
lines.push(
  `**Reduction vs baseline (S1, approach A):** ~${cut}% fewer tokens than loading all 6 originals.`,
);
lines.push("");

lines.push("## Per-scenario detail");
lines.push("");
for (const r of costRows) {
  lines.push(`### ${r.id} — ${r.mode} — ${r.description}`);
  lines.push("");
  for (const approach of ["A", "B"]) {
    const d = r[approach];
    lines.push(`**Approach ${approach}** — total ${fmt(d.total)} (always-loaded ${fmt(d.alwaysTotal)} + on-demand ${fmt(d.onDemandTotal)})`);
    lines.push("");
    lines.push("| file | tokens |");
    lines.push("|---|---:|");
    for (const f of d.always) lines.push(`| ${f.path} | ${fmt(f.tokens)} |`);
    for (const f of d.onDemand) lines.push(`| ${f.path} | ${fmt(f.tokens)} |`);
    lines.push("");
  }
  lines.push(`**Delta (B − A):** ${fmt(r.delta)} tokens — ${r.delta < 0 ? "B cheaper" : r.delta > 0 ? "A cheaper" : "tie"}.`);
  lines.push("");
}

lines.push("## Dispatch overhead (S4) — the A-vs-B structural delta");
lines.push("");
const s4 = costRows.find((r) => r.id === "S4-dispatch-overhead");
lines.push("| | always-loaded files | tokens |");
lines.push("|---|---|---:|");
lines.push(`| A | ${s4.A.always.map((f) => f.path).join(", ")} | ${fmt(s4.A.alwaysTotal)} |`);
lines.push(`| B | ${s4.B.always.map((f) => f.path).join(", ")} | ${fmt(s4.B.alwaysTotal)} |`);
lines.push("");
lines.push(
  `A pays ${fmt(s4.A.alwaysTotal)} to activate a single mode; B pays ${fmt(s4.B.alwaysTotal)} (router + the selected mode file). This is the fixed cost B carries on **every** task to gain a single entry point.`,
);
lines.push("");

lines.push("## Shared on-demand block (identical for A and B)");
lines.push("");
lines.push("| file | tokens |");
lines.push("|---|---:|");
for (const f of sharedBlock) lines.push(`| ${f.path} | ${fmt(f.tokens)} |`);
lines.push(`| **block total** | **${fmt(sharedTotal)}** |`);
lines.push("");
lines.push("This block dominates every design task and is loaded **once**. Because A and B load the same block, single-task totals differ only by the S4 dispatch delta.");
lines.push("");

lines.push("## Baseline — pre-integration (the 'before')");
lines.push("");
lines.push("| skill | SKILL.md | refs tokens | skill total |");
lines.push("|---|---:|---:|---:|");
for (const s of baselineSkills) {
  const refsTotal = s.refs.reduce((a, r) => a + r.tokens, 0);
  lines.push(`| ${s.skill} | ${fmt(s.skillMd.tokens)} | ${fmt(refsTotal)} | ${fmt(s.total)} |`);
}
lines.push(`| **all 6** | | | **${fmt(baselineTotal)}** |`);
lines.push("");
lines.push("Each original carried its own copy of the anti-slop catalog, the checklist, the intake flow, and the direction library. The integration holds one copy in `frontend-shared/`.");
lines.push("");

// --- quality checks (structural, grep-based) ---
const REF_NAMES = [
  "discovery",
  "design-language",
  "aesthetic-directions",
  "component-sourcing",
  "accessibility-motion",
  "quality-checklist",
];
function fileContains(relPath, needle) {
  const abs = join(SDLC_ROOT, relPath);
  try {
    // case-insensitive so "card sickness" matches "Card sickness"
    return readFileSync(abs, "utf8").toLowerCase().includes(needle.toLowerCase());
  } catch {
    return false;
  }
}
const qResults = [];
// Q1 — catalog lives once in design-language; mode skills reference it, don't re-list
const dlHasCatalog = ["P0", "P1", "P2", "card sickness"].every((n) =>
  fileContains("frontend-shared/references/design-language.md", n),
);
const buildRefsDl = fileContains("frontend-build/SKILL.md", "design-language.md");
const redesignRefsDl = fileContains("frontend-redesign/SKILL.md", "design-language.md");
const buildInlineTells = fileContains("frontend-build/SKILL.md", "rounded-2xl shadow-lg");
qResults.push({
  id: "Q1",
  name: "anti-slop catalog exists once",
  pass: dlHasCatalog && buildRefsDl && redesignRefsDl && !buildInlineTells,
  detail: `catalog-in-design-language=${dlHasCatalog}; build refs it=${buildRefsDl}; redesign refs it=${redesignRefsDl}; build does NOT re-list tells=${!buildInlineTells}`,
});
// Q2 — one canonical checklist; mode skills reference it
const checklistHasSections = ["Interaction", "Accessibility", "Motion system"].every((n) =>
  fileContains("frontend-shared/references/quality-checklist.md", n),
);
const modesRefChecklist =
  fileContains("frontend-build/SKILL.md", "quality-checklist.md") &&
  fileContains("frontend-redesign/SKILL.md", "quality-checklist.md");
qResults.push({
  id: "Q2",
  name: "one canonical delivery checklist",
  pass: checklistHasSections && modesRefChecklist,
  detail: `checklist has sections=${checklistHasSections}; both modes reference it=${modesRefChecklist}`,
});
// Q3 — stack assumptions are adapters, not hardcoded
const q3Paths = [
  "frontend-build/SKILL.md",
  "frontend-redesign/SKILL.md",
  "frontend-studio/SKILL.md",
];
const q3Ok = q3Paths.every((p) => fileContains(p, "stacks/"));
qResults.push({
  id: "Q3",
  name: "stack assumptions are adapters",
  pass: q3Ok,
  detail: `all three mode/router files reference stacks/: ${q3Ok}`,
});
// Q4 — A and B load the same 6 references
const buildA = "frontend-build/SKILL.md";
const buildB = "frontend-studio/modes/build.md";
const q4Ok = REF_NAMES.every((n) => fileContains(buildA, `${n}.md`) && fileContains(buildB, `${n}.md`));
qResults.push({
  id: "Q4",
  name: "A and B load the same reference content",
  pass: q4Ok,
  detail: `build SKILL and modes/build.md both reference all 6 refs: ${q4Ok}`,
});

lines.push("## Quality checks (structural)");
lines.push("");
lines.push("Greps verifying the integration's structural claims (Q1–Q4 in `eval.json`). These check consolidation, stack-agnosticism, and A/B content parity — not output taste.");
lines.push("");
lines.push("| ID | check | result | detail |");
lines.push("|---|---|---|---|");
for (const q of qResults) {
  lines.push(`| ${q.id} | ${q.name} | ${q.pass ? "✅ pass" : "❌ fail"} | ${q.detail} |`);
}
lines.push("");
const allQ = qResults.every((q) => q.pass);
lines.push(allQ ? "All quality checks pass." : "Some quality checks FAILED — see detail.");
lines.push("");

lines.push("## Interpretation");
lines.push("");
lines.push("- **S1 / S2 (single task):** A and B differ only by the dispatch delta (S4). A is leaner because it has no router layer.");
lines.push("- **S3 (both modes):** B loads one router + two mode files; A loads two mode SKILLs. If the router is cheaper than the second mode SKILL it replaces, B breaks even or wins here.");
lines.push("- **S4 (dispatch overhead):** the pure structural cost of choosing B over A.");
lines.push("- **Baseline:** the integration removes the six-way duplication. The reduction is the headline value of this work, independent of A vs B.");
lines.push("");

const report = lines.join("\n");
writeFileSync(RESULTS_FILE, report, "utf8");

// stdout summary
console.log(`Tokenizer: ${tokenizerLabel}\n`);
console.log("Scenario            | A         | B         | B-A       | Cheaper");
console.log("---------------------|-----------|-----------|-----------|--------");
for (const r of costRows) {
  const cheaper = r.delta < 0 ? "B" : r.delta > 0 ? "A" : "tie";
  console.log(
    `${r.id.padEnd(20)}| ${String(fmt(r.A.total)).padStart(9)} | ${String(fmt(r.B.total)).padStart(9)} | ${String(fmt(r.delta)).padStart(9)} | ${cheaper}`,
  );
}
console.log("");
console.log(`Baseline (6 originals, full load): ${fmt(baselineTotal)} tokens / ${baselineFileCount} files`);
console.log(`S1 (A) vs baseline: ~${cut}% reduction`);
console.log("");
console.log(`Wrote ${RESULTS_FILE}`);
